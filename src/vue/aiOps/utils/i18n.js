export function getText(language, key, params = {}) {
  // KR과 KO 용어 통일 처리
  const normalizedLanguage = language === 'kr' ? 'ko' : language;
  
  let text = i18nResources[normalizedLanguage]?.[key] || key;
  
  if (params && typeof text === 'string') {
    Object.keys(params).forEach(paramKey => {
      const regex = new RegExp(`{${paramKey}}`, 'g');
      text = text.replace(regex, params[paramKey]);
    });
  }
  
  return text;
}

export function getTextArray(language, key) {
  // KR과 KO 용어 통일 처리
  const normalizedLanguage = language === 'kr' ? 'ko' : language;
  return i18nResources[normalizedLanguage]?.[key] || [];
}

export default i18nResources;